package com.chanct.ThreeOperators.entity;

public class BlackphoneResult {

    String fileName;
    String blackPhone;
    String manageTime;
    String manageResult;
    String interceptTime;
    String timeLimit;
    int type;

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getBlackPhone() {
        return blackPhone;
    }

    public void setBlackPhone(String blackPhone) {
        this.blackPhone = blackPhone;
    }

    public String getManageTime() {
        return manageTime;
    }

    public void setManageTime(String manageTime) {
        this.manageTime = manageTime;
    }

    public String getManageResult() {
        return manageResult;
    }

    public void setManageResult(String manageResult) {
        this.manageResult = manageResult;
    }

    public String getInterceptTime() {
        return interceptTime;
    }

    public void setInterceptTime(String interceptTime) {
        this.interceptTime = interceptTime;
    }

    public String getTimeLimit() {
        return timeLimit;
    }

    public void setTimeLimit(String timeLimit) {
        this.timeLimit = timeLimit;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
